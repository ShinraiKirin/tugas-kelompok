public class Matematika {
    public double pertambahan(double x, double y){
        return x + y;
    }    
    public double pertambahan(double x, double y, double z){
        return x + y + z;
    }
    public double pengurangan(double x, double y){
        return x - y;
    }
    public double pengurangan(double x, double y, double z){
        return x - y - z;
    }
}
